void PortFHandler (void)
{
    uint32_t flag = GPIOIntStatus(GPIO_PORTF_BASE,true);
    if ( (flag & GPIO_INT_PIN_0) == GPIO_INT_PIN_0)
    {
        SysCtlDelay( SysCtlClockGet() * 0.15 );
        GPIOIntClear(GPIO_PORTF_BASE, GPIO_INT_PIN_0);
        tela_inicial = false;
    }
    if (!tela_inicial)
    {
        srand(time(0));
        int i = 0;

        int randomNumber = rand() % (size_list - 1); // 19
        palavra = list_of_words[randomNumber];
        size_palavra = strlen(palavra);


        char* tmp = (char*)malloc(size_palavra * sizeof(char));

        for (i = 0; i < size_palavra; i++)
        {
            tmp[i] = '0';
            UARTCharPut(UART0_BASE, palavra[i]);
        }
        char_acertados = tmp;
        draw_word();
    }
}

